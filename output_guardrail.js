// Input Guardrail Agent to check if the query is maths related or not
const sqlOutputGuardRailAgent = new Agent({
  name: "SQL GuardRail Agent",
  model: "gpt-4o-mini",
  instructions: `You are a strict Data Security and Compliance Officer. Your sole responsibility is to evaluate the output generated by the main database agent before it reaches the employee.

CRITICAL SECURITY POLICY:
Employees are ONLY authorized to view the following generic user information:
1. Name (name)
3. Payment Method (payment_method)
4. Membership Tier (membership)

YOUR TASK:
Analyze the provided text/output. 
- If the output contains ONLY the allowed fields (or safe conversational text explaining a query), set 'isQuerySafe' to true.
- If the output contains ANY unauthorized sensitive data—specifically 'password', 'bank_account_number', 'address', 'gender', 'id', or any other unlisted personal attributes—you MUST set 'isQuerySafe' to false.
- If rejecting, provide a concise 'reason' stating exactly which sensitive fields were detected and blocked.`,
  outputType: z.object({
    isQuerySafe: z.boolean(),
    reason: z.string().optional().describe("Reason for rejecting the query"),
  }),
});

// Main Agent
const agent = new Agent({
  name: "SQL generator agent",
  instructions: `You are an expert Backend Systems Developer specializing in NoSQL databases and the Mongoose ORM. You are an internal tool for Maxverse Corp, assisting backend developers with database operations.

YOUR OBJECTIVES:
1. Translate natural language requests into highly optimized Mongoose ORM queries.
2. Use the 'getDataFromDB' tool to fetch the actual data based on the user's intent.
3. Provide the user with both the precise Mongoose query they need to write in their code, AND a preview of the expected output.
    `,
  tools: [getDataFromDB],
  outputGuardrails: [
    {
      name: "No-SQL Query Guardrail",
      async execute({ agentOutput }) {
        const result = await run(sqlOutputGuardRailAgent, agentOutput);

        // If the guardrail fails, throw custom error with the reason
        if (!result.finalOutput.isQuerySafe) {
          throw new GuardrailRejection(result.finalOutput.reason);
        }

        return {
          outputInfo: result.finalOutput,
          tripwireTriggered: false,
        };
      },
    },
  ],
});

// Main Function
async function main(query) {
  try {
    const result = await run(agent, query);
    console.log(`Agent -> `, result.finalOutput);
  } catch (error) {
    // 1. Check if it's the framework's wrapper error
    if (error.name === "GuardrailExecutionError" && error.error) {
      // 2. Extract the reason from your custom GuardrailRejection buried inside
      const reason = error.error.reason || error.error.message;
      console.log(`\nQuery Declined: ${reason}\n`);
    } else {
      // For any other unexpected crashes
      console.error("System Error:", error.message);
    }
  }
}

